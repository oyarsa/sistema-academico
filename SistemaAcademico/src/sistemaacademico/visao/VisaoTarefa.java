    private void initColaboradores() {
        codToColab = new HashMap<>();
        colabToCod = new HashMap<>();
        ArrayList<HashMap<String, Object>> colaboradores = ControleColaborador.recuperarTodos();

        for (HashMap<String, Object> c : colaboradores) {
            codToColab.put((Integer) c.get("codigo"), (String) c.get("nome"));
            colabToCod.put((String) c.get("nome"), (Integer) c.get("codigo"));
        }
    }

    private void initTiposTrabalho() {
        codToTipo = new HashMap<>();
        tipoToCod = new HashMap<>();
        ArrayList<HashMap<String, Object>> tipos = ControleTipoTrabalho.recuperarTodos();

        for (HashMap<String, Object> t : tipos) {
            codToTipo.put((Integer) t.get("codigo"), (String) t.get("descricao"));
            tipoToCod.put((String) t.get("descricao"), (Integer) t.get("codigo"));
        }
    }

    private void configComboTipos() {
        comboTipo.removeAllItems();
        for (String s : tipoToCod.keySet()) {
            comboTipo.addItem(s);
        }
    }

    private void configComboColabs() {
        comboColaboradores.removeAllItems();
        for (String s : colabToCod.keySet()) {
            comboColaboradores.addItem(s);
        }
    }

    private void atualizarTabela(int linha, HashMap<String, Object> dados) {
        if (linha == -1) {
            ((DefaultTableModel) tableTarefas.getModel())
                    .addRow(new Object[]{});
            linha = tableTarefas.getRowCount() - 1;
        }

        tableTarefas.setValueAt(dados.get("codigo"), linha, 0);
        tableTarefas.setValueAt(dados.get("horaInicio"), linha, 1);
        tableTarefas.setValueAt(dados.get("horaFim"), linha, 2);
        tableTarefas.setValueAt(codToTipo.get((Integer) dados.get("tipoTarefa")), linha, 3);
        tableTarefas.setValueAt(dados.get("observacao"), linha, 4);
    }

    private void carregarTarefas() {
        DefaultTableModel m = (DefaultTableModel) tableTarefas.getModel();
        ArrayList<HashMap<String, Object>> tarefas = ControleTarefa.recuperarTodos(atividade);
        m.setRowCount(0);

        for (HashMap<String, Object> t : tarefas) {
            m.addRow(new Object[]{t.get("codigo"), t.get("horaInicio"),
                t.get("horaFim"), codToTipo.get((Integer) t.get("tipoTarefa")),
                t.get("observacao")});
        }
    }
