    private void carregarProjetos() {
        DefaultTableModel m = (DefaultTableModel) tableProjetos.getModel();
        ArrayList<HashMap<String, Object>> projetos = ControleProjeto.recuperarTodos();
        m.setRowCount(0);

        for (HashMap<String, Object> p : projetos) {
            m.addRow(new Object[]{p.get("codigo"), p.get("nome")});
        }

    }
